package data;

public class data {
    public int Period;
    public int T;
    public int Sum;
    public double TotalSetup;
    public double SavingSum;
    public double SavingFee;
    public double Diff;
    public String Status;
    public double TotalCost;

    public int getPeriod() {
        return Period;
    }

    public void setPeriod(int period) {
        Period = period;
    }

    public int getT() {
        return T;
    }

    public void setT(int t) {
        T = t;
    }

    public int getSum() {
        return Sum;
    }

    public void setSum(int sum) {
        Sum = sum;
    }

    public double getTotalSetup() {
        return TotalSetup;
    }

    public void setTotalSetup(double totalSetup) {
        TotalSetup = totalSetup;
    }

    public double getSavingSum() {
        return SavingSum;
    }

    public void setSavingSum(double savingSum) {
        SavingSum = savingSum;
    }

    public double getSavingFee() {
        return SavingFee;
    }

    public void setSavingFee(double savingFee) {
        SavingFee = savingFee;
    }

    public double getDiff() {
        return Diff;
    }

    public void setDiff(double diff) {
        Diff = diff;
    }

    public String getStatus() {
        return Status;
    }

    public void setStatus(String status) {
        Status = status;
    }

    public double getTotalCost() {
        return TotalCost;
    }

    public void setTotalCost(double totalCost) {
        TotalCost = totalCost;
    }
}
