package data;

public class data {
    public String Period;
    public int T;
    public double TotalSetup;
    public double SavingSum;
    public double SavingFee;
    public double Diff;
    public String Status;
    public double AccumCost;
    public double Quantity;

    public String getPeriod() {
        return Period;
    }

    public void setPeriod(String period) {
        Period = period;
    }

    public int getT() {
        return T;
    }

    public void setT(int t) {
        T = t;
    }

    public double getTotalSetup() {
        return TotalSetup;
    }

    public void setTotalSetup(double totalSetup) {
        TotalSetup = totalSetup;
    }

    public double getSavingSum() {
        return SavingSum;
    }

    public void setSavingSum(double savingSum) {
        SavingSum = savingSum;
    }

    public double getSavingFee() {
        return SavingFee;
    }

    public void setSavingFee(double savingFee) {
        SavingFee = savingFee;
    }

    public double getDiff() {
        return Diff;
    }

    public void setDiff(double diff) {
        Diff = diff;
    }

    public String getStatus() {
        return Status;
    }

    public void setStatus(String status) {
        Status = status;
    }

    public double getAccumCost() {
        return AccumCost;
    }

    public void setAccumCost(double accumCost) {
        AccumCost = accumCost;
    }

    public double getQuantity() {
        return Quantity;
    }

    public void setQuantity(double quantity) {
        Quantity = quantity;
    }
}
